    <span class="author">
      <font size="30px">李白:</font>李白（701年-762年）,字太白，号青莲居士，又号“谪仙人”，祖籍陇西成纪（今甘肃省秦安县），唐代伟大的浪漫主义诗人，被后人誉为“诗仙”，与杜甫合称为“大李杜”。李白爽朗大方，爱饮酒作诗，喜交友，深受黄老列庄思想影响，有《李太白集》传世，诗作中多以醉时写的。李白所作词赋，宋人已有传记（如文莹《湘山野录》卷上），就其开创意义及艺术成就而言，“李白词”享有极为崇高的地位。代表作有《望庐山瀑布》《行路难》《蜀道难》等。
    </span>
